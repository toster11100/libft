#include "libft.h"

int	ft_atoi(const char *str)
{
	long int	i;
	int			neg;

	i = 0;
	neg = 1;
	while (*str == ' ' || *str > 8 && *str < 14)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		if (i >= 922337203685477600)
			return (-1);
		if ((i * neg) <= -922337203685477)
			return (0);
		i *= 10;
		i += *str - '0';
		str++;
	}
	return (i * neg);
}
